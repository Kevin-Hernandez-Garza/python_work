# WORKING WITH LISTS

# for loop 
cities = ["boston", "orlando", "dallas", "kansas city", "aurora", "denver"]
# for every "city" in the list of cities, print out the name
for city in cities:
    print(f"Bucket List vacation is: {city.title()}\n")
print("Let's plan it out!\n")


